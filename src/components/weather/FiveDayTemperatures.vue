    <b-overlay :show="useAutoGeneratedLocation && !Object.keys(fiveDayWeatherForecast).length" rounded="sm">
    <b-card title="Five Day Weather Forecast" :sub-title="cityStateLocation ? cityStateLocation.city + ', ' + cityStateLocation.principalSubdivision : null">
      <b-card-text>
        {{ fiveDayWeatherForecast }}
